package com.jcrocherdevDoctor2;

public class DoctorList {

	private int number;
	private String name;
	
	
	public DoctorList(int number, String name) {
		this.number = number;
		this.name = name;
	}
	
	public int getNumber() {
		return number;
	}
	
	public String getName() {
		return name;
	}

	
}
